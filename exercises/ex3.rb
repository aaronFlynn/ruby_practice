# basic maths

# Using string interpolation to output the number in the string
puts 'Lets count some chickens!!'
puts "Hens #{25 + 30 / 6}"
puts "Roosters #{100 - 25 * 3 % 4}"

# Showing Maths BOMDAS
puts 'Now Lets count some eggs!!'
puts 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6

# Looking at comparisons
puts 'Is it true that 3 + 2 < 5 - 7 ?'
puts 3 + 2 < 5 - 7

# More interpolation
puts "Whats 3 + 2? #{3 + 2}"
puts "Whats 5 - 7? #{5 - 7}"

puts 'Oh, thats false'

puts 'How about some more'

# Greater than /less than comparisons
puts "Is it greater? #{5 > -2}"
puts "Is it greater or equal? #{5 >= -2}"
puts "Is it less or equal? #{5 <= -2}"
